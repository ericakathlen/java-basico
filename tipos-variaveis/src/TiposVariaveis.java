public class TiposVariaveis {
    // public static void variaveis
    public static void main(String[] args){
        // Principais variaveis em Java e como declara - las
        String meuNome = "Erica";
        int minhaIdade = 23;
        double minhaAltura = 1.58;
        char sexo = 'M';
        boolean estouFeliz = true; // 1 bit - recebe valores apenas de true or false
        
        //classe (objeto)       antecede um new e o nome da classe e seus paramentros
       // Date dataNascimento = new Date()

        System.out.println(meuNome);
        System.out.println(minhaIdade);
        System.out.println(minhaAltura);
        System.out.println(sexo);
        System.out.println(estouFeliz);
       // System.out.println(dataNascimento);
    }
}
